"""
39. A importância de R$ 780.000,00 será dividida entre três ganhadores de
um concurso. Sendo que da quantia total:
* O primeiro ganhador receberá 46%;
* O segundo receberá 32%;
* O terceiro receberá o restante.
"""
primeiro = 780_000.00 * 0.46
segundo = 780_000.00 * 0.32
terceiro = 780_000.00 * 0.22
print(f'O primeiro ganhador recebeu R${primeiro:.2f}')
print(f'O segundo ganhador recebeu R${segundo:.2f}')
print(f'O terceiro ganhador recebeu R${terceiro:.2f}')
